//Operadores logicos
/**
 * && : Si los dos son verdaderos
 * || : con que uno sea verdadero
 * != Negarcion
 */

let resultado = true && true
console.log(resultado)

let compara = 20 > 30 && 10 < 20
console.log(compara)

let or = (true && false) || (true && true)
console.log(or)

let negacion = !(true && true) && (true || false)
console.log(negacion)